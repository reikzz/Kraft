package org.kraft.input

/**
 * Enumeration of abstract game actions mapped to physical inputs.
 */
enum class GameAction {
    MOVE_FORWARD,
    MOVE_BACKWARD,
    MOVE_LEFT,
    MOVE_RIGHT,
    JUMP,
    DESTROY_BLOCK,
    PLACE_BLOCK,
    TOGGLE_CURSOR,
    SELECT_SLOT_1,
    SELECT_SLOT_2,
    SELECT_SLOT_3,
    SELECT_SLOT_4,
    SELECT_SLOT_5,
    SELECT_SLOT_6,
    SELECT_SLOT_7,
    SELECT_SLOT_8,
    SELECT_SLOT_9
}
